function createMap<K, V>(keyIndex: number, valueIndex: number) {
  return new Map<K, V>(
    MAPPING.flatMap((v) => v[0].map((x) => [x, ...v.slice(1)])).map((v) => [
      v[keyIndex] as K,
      v[valueIndex] as V,
    ]),
  );
}

